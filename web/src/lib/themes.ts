/* ------------------------------------------------------------------ */
/* Theme catalog — popular Visual Studio Code color schemes mapped onto */
/* arbos's semantic tokens. Each theme overrides the `--color-*` custom */
/* properties defined in index.css at runtime, so swapping a theme just */
/* repaints the whole panel (chat, composer, diffs, code highlighting). */
/* ------------------------------------------------------------------ */

/** The semantic color slots a theme must fill (one per `--color-*` token). */
export type ColorToken =
  | "canvas"
  | "bar"
  | "card"
  | "panel"
  | "line"
  | "text"
  | "bright"
  | "muted"
  | "faint"
  | "accent"
  | "green"
  | "red"
  | "warn"
  | "add-bg"
  | "del-bg"
  | "btn"
  | "syntax-comment"
  | "syntax-string"
  | "syntax-number"
  | "syntax-keyword";

export interface Theme {
  id: string;
  name: string;
  /** Drives `color-scheme` so native controls / scrollbars match. */
  dark: boolean;
  colors: Record<ColorToken, string>;
}

